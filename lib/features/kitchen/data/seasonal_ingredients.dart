String getCurrentSeasonMonthName() {
  return monthNames[DateTime.now().month];
}

List<String> getCurrentSeasonalIngredients() {
  return seasonalIngredientsByMonth[DateTime.now().month] ?? [];
}

List<String> getAllKnownProduceIngredients() {
  final ingredients = <String>{};

  ingredients.addAll(allKnownProduceIngredients);

  for (final monthlyIngredients in seasonalIngredientsByMonth.values) {
    ingredients.addAll(monthlyIngredients);
  }

  final sortedIngredients = ingredients.toList()..sort();

  return sortedIngredients;
}

bool ingredientNameMatchesKnownProduce(String ingredientName) {
  final knownProduceIngredients = getAllKnownProduceIngredients();

  return knownProduceIngredients.any((produce) {
    return ingredientNameMatchesSeasonalItem(
      ingredientName: ingredientName,
      seasonalItem: produce,
    );
  });
}

bool ingredientNameMatchesSeasonalItem({
  required String ingredientName,
  required String seasonalItem,
}) {
  final normalizedIngredient = normalizeSeasonText(ingredientName);
  final normalizedSeasonalItem = normalizeSeasonText(seasonalItem);

  if (normalizedIngredient.isEmpty || normalizedSeasonalItem.isEmpty) {
    return false;
  }

  return _containsAsWords(
        value: normalizedIngredient,
        searchedValue: normalizedSeasonalItem,
      ) ||
      _containsAsWords(
        value: normalizedSeasonalItem,
        searchedValue: normalizedIngredient,
      );
}

bool _containsAsWords({required String value, required String searchedValue}) {
  final normalizedValue = ' ${_singularizeWords(value)} ';
  final normalizedSearchedValue = ' ${_singularizeWords(searchedValue)} ';

  return normalizedValue.contains(normalizedSearchedValue);
}

String _singularizeWords(String value) {
  return value
      .split(' ')
      .map((word) {
        if (word.length <= 3) {
          return word;
        }

        if (word.endsWith('aux')) {
          return '${word.substring(0, word.length - 3)}al';
        }

        if (word.endsWith('s')) {
          return word.substring(0, word.length - 1);
        }

        return word;
      })
      .join(' ');
}

String normalizeSeasonText(String value) {
  return value
      .toLowerCase()
      .trim()
      .replaceAll('œ', 'oe')
      .replaceAll('æ', 'ae')
      .replaceAll('é', 'e')
      .replaceAll('è', 'e')
      .replaceAll('ê', 'e')
      .replaceAll('ë', 'e')
      .replaceAll('à', 'a')
      .replaceAll('â', 'a')
      .replaceAll('ù', 'u')
      .replaceAll('û', 'u')
      .replaceAll('î', 'i')
      .replaceAll('ï', 'i')
      .replaceAll('ô', 'o')
      .replaceAll('ö', 'o')
      .replaceAll('ç', 'c')
      .replaceAll(RegExp(r"[’']"), ' ')
      .replaceAll(RegExp(r'[^a-z0-9\s-]'), ' ')
      .replaceAll(RegExp(r'\s+'), ' ')
      .trim();
}
